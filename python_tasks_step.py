print(c, b, a, sep='')


# 29. –ß–µ—Ç—ã—Ä—ë—Ö–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ
""" –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É –¥–ª—è –Ω–∞—Ö–æ–∂–¥–µ–Ω–∏—è —Ü–∏—Ñ—Ä —á–µ—Ç—ã—Ä—ë—Ö–∑–Ω–∞—á–Ω–æ–≥–æ —á–∏—Å–ª–∞.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
-> –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–µ —á–µ—Ç—ã—Ä—ë—Ö–∑–Ω–∞—á–Ω–æ–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ.
–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
-> –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.
Sample Input 1:
 3281
Sample Output 1:
 –¶–∏—Ñ—Ä–∞ –≤ –ø–æ–∑–∏—Ü–∏–∏ —Ç—ã—Å—è—á —Ä–∞–≤–Ω–∞ 3
 –¶–∏—Ñ—Ä–∞ –≤ –ø–æ–∑–∏—Ü–∏–∏ —Å–æ—Ç–µ–Ω —Ä–∞–≤–Ω–∞ 2
 –¶–∏—Ñ—Ä–∞ –≤ –ø–æ–∑–∏—Ü–∏–∏ –¥–µ—Å—è—Ç–∫–æ–≤ —Ä–∞–≤–Ω–∞ 8
 –¶–∏—Ñ—Ä–∞ –≤ –ø–æ–∑–∏—Ü–∏–∏ –µ–¥–∏–Ω–∏—Ü —Ä–∞–≤–Ω–∞ 1       """

num = int(input())
fourth_digit = num % 10
third_digit = (num % 100) // 10
