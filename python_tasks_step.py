# 77. –ò–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ —á–∏—Å–ª–æ
""" –ù–∞–∑–æ–≤–µ–º —á–∏—Å–ª–æ –∏–Ω—Ç–µ—Ä–µ—Å–Ω—ã–º, –µ—Å–ª–∏ –≤ –Ω–µ–º —Ä–∞–∑–Ω–æ—Å—Ç—å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π –∏ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π —Ü–∏—Ñ—Ä—ã —Ä–∞–≤–Ω—è–µ—Ç—Å—è —Å—Ä–µ–¥–Ω–µ–π –ø–æ –≤–µ–ª–∏—á–∏–Ω–µ —Ü–∏—Ñ—Ä–µ. 
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –æ–ø—Ä–µ–¥–µ–ª—è–µ—Ç, –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ —á–∏—Å–ª–æ –∏–ª–∏ –Ω–µ—Ç. 
–ï—Å–ª–∏ —á–∏—Å–ª–æ –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ, —Å–ª–µ–¥—É–µ—Ç –≤—ã–≤–µ—Å—Ç–∏ ¬´–ß–∏—Å–ª–æ –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ¬ª, –∏–Ω–∞—á–µ - ¬´–ß–∏—Å–ª–æ –Ω–µ–∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ¬ª.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö: –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Ü–µ–ª–æ–µ —Ç—Ä–µ—Ö–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ.
–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö: –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏. """

n = int(input())
digit_1 = n // 100
digit_2 = (n // 10) % 10
digit_3 = n % 10
sum_digit = digit_1 + digit_2 + digit_3
max_digit = max(digit_1, digit_2, digit_3)
min_digit = min(digit_1, digit_2, digit_3)
mid_digit = sum_digit - (max_digit + min_digit)

if max_digit - min_digit == mid_digit:
    print('–ß–∏—Å–ª–æ –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ')
else:
    print('–ß–∏—Å–ª–æ –Ω–µ–∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ–µ')

